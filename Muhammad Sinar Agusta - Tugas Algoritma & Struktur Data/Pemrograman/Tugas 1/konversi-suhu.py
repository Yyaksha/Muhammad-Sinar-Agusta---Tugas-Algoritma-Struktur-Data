Reamur = float(input("Masukkan suhu Reamur :"))
Celcius = 0.0
Fahrenheit = 0.0
Kelvin = 0.0

Ce = 5/4 * Reamur
Fa = 9/4 * Reamur + 32
Kel = Reamur * 5/4 + 273.15

print("Suhu dari Reamur --> Celcius : %.2f"% Ce)
print("Suhu dari Reamur --> Fahrenheit : %.2f"% Fa)
print("Suhu dari Reamur --> Kelvin : %.2f"% Kel)