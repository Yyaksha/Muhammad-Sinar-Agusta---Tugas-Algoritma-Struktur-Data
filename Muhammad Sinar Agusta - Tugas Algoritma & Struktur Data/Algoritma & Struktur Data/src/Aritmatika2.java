public class Aritmatika2 {
    public static void main(String[] args){
        
        int variabel1 = 10;
        int variabel2 = 20;

        
        // PENJUMLAHAN 
        int hasilPenjumlahan;

        hasilPenjumlahan = variabel1 + variabel2;
        System.out.printf(" hasil penjumlahan dari %d + %d = %d \n",variabel1,variabel2,hasilPenjumlahan);

        // PENGURANGAN
        int hasilPengurangan;

        hasilPengurangan = variabel1 - variabel2;
        System.out.printf(" hasil pengurangan dari %d - %d = %d \n",variabel1,variabel2,hasilPengurangan);

        // PERKALIAN
        int hasilPerkalian;
    
        hasilPerkalian = variabel1 * variabel2;
        System.out.printf(" hasil perkalian dari %d * %d = %d \n",variabel1,variabel2,hasilPerkalian);

        // PEMBAGIAN
        int hasilPembagian;
        hasilPembagian = variabel1 / variabel2;
        System.out.printf(" hasil pembagian dari %d / %d = %d \n",variabel1,variabel2,hasilPembagian);

        // MODULUS
        int hasilModulus;
        hasilModulus = variabel1 % variabel2;
        System.out.printf(" hasil modulus dari %d %% %d = %d \n",variabel1,variabel2,hasilModulus);
    
    }
    
}
